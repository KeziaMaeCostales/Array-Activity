// Step 1: Create an object called car with properties type, model, and color
let car = {
    type: "Sedan",
    model: "Corolla",
    color: "Red"
  };
  console.log("Initial car object:", car); 
  
  // Step 2: Check the type of the object
  console.log("Type of car:", typeof car); 
  
  // Step 3: Update the type property to "Toyota"
  car.type = "Toyota";
  console.log("Updated car object:", car); 
  
  
  // Step 4: Add a new property wheels with the value 4
  car.wheels = 4;
  console.log("Car object with wheels:", car); 

  
